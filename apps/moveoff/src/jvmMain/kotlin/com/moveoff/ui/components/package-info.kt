 * - 对话框、Toast 通知
 * - 进度条、加载动画
 *
 * ### 复合组件
 * - 文件浏览器（列表+导航+工具栏）
 * - 服务器配置表单
 * - 冲突解决卡片
 *
 * ## 设计原则
 * - 单一职责：每个组件只做一件事
 * - 可配置：通过参数控制外观和行为
