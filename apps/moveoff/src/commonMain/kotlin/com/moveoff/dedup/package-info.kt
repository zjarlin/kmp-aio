/**
 * 文件去重层 - 基于内容哈希的重复文件检测与管理
 *
 * ## 职责
 * - 扫描并检测内容完全相同的文件
 * - 提供删除、硬链接等去重策略
 * - 计算可节省的存储空间
 *
 * ## 核心组件
 * - [DeduplicationManager] - 去重管理器
 * - [DuplicateGroup] - 重复文件组
 * - [DedupScanResult] - 扫描结果
 *
 * ## 去重策略
 * 1. **安全删除** - 删除重复文件，保留原始文件（可移到回收站）
 * 2. **智能删除** - 根据文件位置、名称等智能选择保留的文件
 * 3. **硬链接** - 用硬链接替换重复文件，保留文件结构但节省空间
 *
 * ## 使用场景
 * - 用户误复制了多个相同文件
 * - 整理同步目录释放空间
 * - 合并多个来源的相同文件
 *
 * ## 安全设计
 * - 优先保留同步目录内的文件
 * - 删除前创建备份
 * - 支持模拟运行（dry-run）
 *
 * @author zjarlin
 * @since 1.0.0
 */
