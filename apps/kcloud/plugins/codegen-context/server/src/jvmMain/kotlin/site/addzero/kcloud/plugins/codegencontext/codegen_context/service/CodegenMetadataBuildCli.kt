                host = tcp.host,
                port = tcp.port,
                unitId = tcp.unitId,
                timeoutMs = tcp.timeoutMs,
                retries = tcp.retries,
            ),
        mqtt =
            KspModbusMqttTransportDefaults(
                brokerUrl = mqtt.brokerUrl,
                clientId = mqtt.clientId,
                requestTopic = mqtt.requestTopic,
                responseTopic = mqtt.responseTopic,
                qos = mqtt.qos,
                timeoutMs = mqtt.timeoutMs,
                retries = mqtt.retries,
            ),
    )

/**
 * 处理modbus传输类型。
 */
private fun ModbusTransportKind.toKspTransportKind(): KspTransportKind =
    when (this) {
