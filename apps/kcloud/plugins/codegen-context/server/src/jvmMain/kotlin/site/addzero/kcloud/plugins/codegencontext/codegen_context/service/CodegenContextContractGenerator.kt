private const val GENERATED_MARKER = "Generated by codegen-context. DO NOT EDIT."
private const val MCU_DEVICE_PACKAGE = "site.addzero.kcloud.plugins.mcuconsole.modbus.device"
private const val METADATA_TABLE = "codegen_context_modbus_contract"

@Single
class CodegenContextContractGenerator(
    private val dataSource: DataSource,
) {
    fun generate(
        context: CodegenContextDetailDto,
    ): GenerateContractsResponseDto {
        if (context.consumerTarget != CodegenConsumerTarget.MCU_CONSOLE) {
            throw BusinessValidationException("Only MCU_CONSOLE is supported in V1.")
        }
        val contextId = context.id ?: throw BusinessValidationException("Context id is required for generation.")
        val settings = context.resolveGenerationSettings()
        val rendered = render(context, settings.workspaceRoot)
        val generatedFiles = (writeRenderedFiles(rendered, settings) + writeTransportArtifacts(rendered, settings)).sorted()
        upsertMetadataSnapshot(context, rendered)
        return GenerateContractsResponseDto(
            contextId = contextId,
            generatedFiles = generatedFiles,
            message = "Generated ${generatedFiles.size} artifacts for ${context.code}.",
        )
    }

    internal fun render(
        context: CodegenContextDetailDto,
        _workspaceRoot: Path,
    ): RenderedContracts {
        val services = context.toModbusServices()
        val artifacts =
            ModbusKotlinContractGenerator.render(
                ModbusKotlinContractGenerationRequest(
                    services = services,
                    fileHeader = GENERATED_MARKER,
                    dtoCustomizations = buildDtoCustomizations(context),
                ),
            )
        val artifactsByFile = artifacts.associateBy(GeneratedArtifact::fileName)
        val registerDtos =
            artifacts
                .filter { artifact -> artifact.fileName.endsWith("Registers") }
                .associate { artifact ->
                    "${artifact.fileName}.kt" to artifact.content.normalizeGeneratedKotlin()
                }
        return RenderedContracts(
            deviceApi =
                artifactsByFile["DeviceApi"]?.content?.normalizeGeneratedKotlin()
                    ?: renderEmptyInterface("DeviceApi"),
            deviceWriteApi =
                artifactsByFile["DeviceWriteApi"]?.content?.normalizeGeneratedKotlin()
                    ?: renderEmptyInterface("DeviceWriteApi"),
            registerDtos = registerDtos,
            metadataPayload = ModbusMetadataJsonCodec.encodeServices(services),
            services = services,
            transport = context.modbusTransport(),
        )
    }

    private fun buildDtoCustomizations(
        context: CodegenContextDetailDto,
    ): Map<String, ModbusDtoCustomization> {
        return context.schemas
            .filter { schema -> schema.direction.name == "READ" && !schema.modelName.isNullOrBlank() }
            .associate { schema ->
                val modelName = requireNotNull(schema.modelName)
                "$MCU_DEVICE_PACKAGE.${modelName}Registers" to
                    ModbusDtoCustomization(
                        summary = schema.description.cleanDocSummary() ?: schema.name,
                        propertyDocs =
                            schema.fields.associate { field ->
                                field.propertyName to (field.description.cleanDocSummary() ?: field.name)
                            },
                    )
            }
    }

    private fun writeRenderedFiles(
        rendered: RenderedContracts,
        settings: ResolvedGenerationSettings,
    ): List<String> {
        settings.serverPackageDir.createDirectories()
        settings.sharedPackageDir.createDirectories()
        writeFile(settings.serverPackageDir.resolve("DeviceApi.kt"), rendered.deviceApi)
        writeFile(settings.serverPackageDir.resolve("DeviceWriteApi.kt"), rendered.deviceWriteApi)
        settings.sharedPackageDir.listDirectoryEntries("*.kt").forEach { existing ->
            if (existing.name.endsWith("Registers.kt")) {
                existing.deleteIfExists()
            }
        }
        rendered.registerDtos.forEach { (fileName, content) ->
            writeFile(settings.sharedPackageDir.resolve(fileName), content)
        }
        return buildList {
            add(settings.serverPackageDir.resolve("DeviceApi.kt").toAbsolutePath().normalize().toString())
            add(settings.serverPackageDir.resolve("DeviceWriteApi.kt").toAbsolutePath().normalize().toString())
            rendered.registerDtos.keys.sorted().forEach { fileName ->
                add(settings.sharedPackageDir.resolve(fileName).toAbsolutePath().normalize().toString())
            }
        }
    }

    private fun writeTransportArtifacts(
        rendered: RenderedContracts,
        settings: ResolvedGenerationSettings,
    ): List<String> {
        val services = rendered.services.toKspServiceModels()
        val transport = rendered.transport.toArtifactKspTransportKind()
        val generatedFiles = mutableListOf<String>()
        cleanupGeneratedPackageDir(settings.gatewayOutputRoot, transport.generatedPackage.replace('.', '/'))
        val serverRouteMode =
            if (settings.springRouteOutputRoot == null) {
                ModbusServerRouteMode.DIRECT_KTOR
            } else {
                ModbusServerRouteMode.SPRING_SOURCE
            }
        generatedFiles +=
            writeKspArtifactsWithPaths(
                settings.gatewayOutputRoot,
                ModbusArtifactRenderer.renderServerArtifacts(
                    transport = transport,
                    services = services,
                    transportDefaults = settings.transportDefaults,
                    serverRouteMode = serverRouteMode,
                ),
            )

        settings.springRouteOutputRoot?.let { outputRoot ->
            cleanupGeneratedPackageDir(outputRoot, transport.generatedPackage.replace('.', '/'))
            generatedFiles +=
                writeKspArtifactsWithPaths(
                    outputRoot,
                    ModbusArtifactRenderer.renderSpringRouteSourceArtifacts(transport, services),
                )
        }

        if (settings.apiClientOutputRoot != null && settings.apiClientPackageName != null) {
            cleanupGeneratedPackageDir(settings.apiClientOutputRoot, settings.apiClientPackageName.replace('.', '/'))
            generatedFiles +=
                writeKspArtifactsWithPaths(
                    settings.apiClientOutputRoot,
                    ModbusArtifactRenderer.renderKtorfitClientArtifacts(
                        transport = transport,
                        services = services,
                        packageName = settings.apiClientPackageName,
                    ),
                )
        }

        settings.cOutputRoot?.let { outputRoot ->
            cleanupGeneratedPackageDir(outputRoot, "generated/modbus/${rendered.transport.transportId}")
            val cArtifacts =
                buildList {
                    services.forEach { service ->
                        addAll(ModbusArtifactRenderer.renderServiceContractArtifacts(service, settings.transportDefaults))
                        addAll(
                            ModbusArtifactRenderer
                                .renderMarkdownArtifacts(service, settings.transportDefaults)
                                .filter { artifact -> artifact.extensionName == "c" },
                        )
                    }
                    addAll(
                        ModbusArtifactRenderer.renderTransportContractArtifacts(
                            transport = transport,
                            services = services,
                            transportDefaults = settings.transportDefaults,
                        ),
                    )
                }
            generatedFiles += writeKspArtifactsWithPaths(outputRoot, cArtifacts)
        }

        settings.markdownOutputRoot?.let { outputRoot ->
            cleanupGeneratedMarkdownArtifacts(outputRoot, rendered.transport)
            val markdownArtifacts =
                services.flatMap { service ->
                    ModbusArtifactRenderer
                        .renderMarkdownArtifacts(service, settings.transportDefaults)
                        .filter { artifact -> artifact.extensionName == "md" }
                }
            generatedFiles += writeKspArtifactsWithPaths(outputRoot, markdownArtifacts)
        }

        return generatedFiles.sorted()
    }

    private fun upsertMetadataSnapshot(
        context: CodegenContextDetailDto,
        rendered: RenderedContracts,
    ) {
        dataSource.connection.use { connection ->
            connection.autoCommit = false
            try {
                val now = Instant.now()
                connection.prepareStatement(
                    """
                    UPDATE $METADATA_TABLE
                    SET selected = 0
                    WHERE consumer_target = ? AND transport = ?
                    """.trimIndent(),
                ).use { statement ->
                    statement.setString(1, context.consumerTarget.name)
                    statement.setString(2, rendered.transport.transportId)
                    statement.executeUpdate()
                }
                val insertSql =
                    """
                    INSERT INTO $METADATA_TABLE (
                        context_id,
                        context_code,
                        context_name,
                        enabled,
                        consumer_target,
                        protocol_template_code,
                        transport,
                        selected,
                        payload,
                        updated_at
                    ) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
                    """.trimIndent()
                connection.prepareStatement(insertSql.appendUpsertClause(connection)).use { statement ->
                    statement.setLong(1, requireNotNull(context.id))
                    statement.setString(2, context.code)
                    statement.setString(3, context.name)
                    statement.setInt(4, if (context.enabled) 1 else 0)
                    statement.setString(5, context.consumerTarget.name)
                    statement.setString(6, context.protocolTemplateCode.orEmpty())
                    statement.setString(7, rendered.transport.transportId)
                    statement.setInt(8, 1)
                    statement.setString(9, rendered.metadataPayload)
                    statement.setUpdatedAt(10, connection, now)
                    statement.executeUpdate()
                }
                connection.commit()
            } catch (throwable: Throwable) {
                connection.rollback()
                throw throwable
            } finally {
                connection.autoCommit = true
            }
        }
    }

    private fun renderEmptyInterface(
        interfaceSimpleName: String,
    ): String =
        """
        // $GENERATED_MARKER
        package $MCU_DEVICE_PACKAGE

        interface $interfaceSimpleName
        """.trimIndent().normalizeGeneratedKotlin()

    private fun writeFile(
        target: Path,
        content: String,
    ) {
        target.parent?.createDirectories()
        Files.writeString(
            target,
            content,
            StandardOpenOption.CREATE,
            StandardOpenOption.TRUNCATE_EXISTING,
            StandardOpenOption.WRITE,
        )
    }
}

internal data class RenderedContracts(
    val deviceApi: String,
    val deviceWriteApi: String,
    val registerDtos: Map<String, String>,
    val metadataPayload: String,
    val services: List<ModbusServiceModel>,
    val transport: ModbusTransportKind,
)

private fun CodegenContextDetailDto.toModbusServices(): List<ModbusServiceModel> {
    val transport = modbusTransport()
    val sortedSchemas = schemas.sortedBy(CodegenSchemaDto::sortIndex)
    val services = mutableListOf<ModbusServiceModel>()

    val readOperations =
        sortedSchemas
            .filter { schema -> schema.direction.name == "READ" }
            .map { schema -> schema.toReadOperation(transport) }
            .let(ModbusContractDefaultsResolver::resolveOperationIdsAndQuantities)
    if (readOperations.isNotEmpty()) {
        services +=
            ModbusServiceModel(
                interfacePackage = MCU_DEVICE_PACKAGE,
                interfaceSimpleName = "DeviceApi",
                interfaceQualifiedName = "$MCU_DEVICE_PACKAGE.DeviceApi",
                serviceId = ModbusContractDefaultsResolver.defaultServiceId("DeviceApi"),
                summary = name,
                basePath = "/api/modbus",
                transport = transport,
                doc = ModbusDocModel(summary = description.cleanDocSummary() ?: name),
                operations = readOperations,
            )
    }

    val writeOperations =
        sortedSchemas
            .filter { schema -> schema.direction.name == "WRITE" }
            .map { schema -> schema.toWriteOperation(transport) }
            .let(ModbusContractDefaultsResolver::resolveOperationIdsAndQuantities)
    if (writeOperations.isNotEmpty()) {
        services +=
            ModbusServiceModel(
                interfacePackage = MCU_DEVICE_PACKAGE,
                interfaceSimpleName = "DeviceWriteApi",
                interfaceQualifiedName = "$MCU_DEVICE_PACKAGE.DeviceWriteApi",
                serviceId = ModbusContractDefaultsResolver.defaultServiceId("DeviceWriteApi"),
                summary = name,
                basePath = "/api/modbus",
                transport = transport,
                doc = ModbusDocModel(summary = description.cleanDocSummary() ?: name),
                operations = writeOperations,
            )
    }

    return services
}

private fun CodegenSchemaDto.toReadOperation(
    transport: ModbusTransportKind,
): ModbusOperationModel {
    val resolvedModelName = requireNotNull(modelName) { "Validated READ schema requires modelName." }
    return ModbusOperationModel(
        methodName = methodName,
        operationId = "",
        functionCodeName = functionCode.name,
        address = baseAddress,
        quantity = -1,
        requestClassName = buildRequestClassName("DeviceApi", transport, methodName),
        requestQualifiedName = buildRequestQualifiedName("DeviceApi", transport, methodName),
        parameters = emptyList(),
        returnType =
            ModbusReturnTypeModel(
                qualifiedName = "$MCU_DEVICE_PACKAGE.${resolvedModelName}Registers",
                simpleName = "${resolvedModelName}Registers",
                kind = ModbusReturnKind.DTO,
                docSummary = description.cleanDocSummary() ?: name,
                properties = fields.sortedBy(CodegenFieldDto::sortIndex).map(CodegenFieldDto::toPropertyModel),
            ),
        doc = ModbusDocModel(summary = description.cleanDocSummary() ?: name),
    )
}

private fun CodegenSchemaDto.toWriteOperation(
    transport: ModbusTransportKind,
): ModbusOperationModel =
    ModbusOperationModel(
        methodName = methodName,
        operationId = "",
        functionCodeName = functionCode.name,
        address = baseAddress,
        quantity = -1,
        requestClassName = buildRequestClassName("DeviceWriteApi", transport, methodName),
        requestQualifiedName = buildRequestQualifiedName("DeviceWriteApi", transport, methodName),
        parameters = fields.sortedBy(CodegenFieldDto::sortIndex).mapIndexed { index, field -> field.toParameterModel(index) },
        returnType =
            ModbusReturnTypeModel(
                qualifiedName = "site.addzero.device.protocol.modbus.model.ModbusCommandResult",
                simpleName = "ModbusCommandResult",
                kind = ModbusReturnKind.COMMAND_RESULT,
            ),
        doc = ModbusDocModel(summary = description.cleanDocSummary() ?: name),
    )

private fun CodegenFieldDto.toPropertyModel(): ModbusPropertyModel =
    ModbusPropertyModel(
        name = propertyName,
        qualifiedType = transportType.kotlinQualifiedTypeName(),
        valueKind = transportType.valueKind(),
        field =
            ModbusFieldModel(
                codecName = transportType.codecName(),
                registerOffset = registerOffset,
                bitOffset = bitOffset,
                length = length,
                registerWidth = transportType.registerWidth(length),
            ),
        doc = description.cleanDocSummary() ?: name,
    )

private fun CodegenFieldDto.toParameterModel(order: Int): ModbusParameterModel =
    ModbusParameterModel(
        name = propertyName,
        qualifiedType = transportType.kotlinQualifiedTypeName(),
        valueKind = transportType.valueKind(),
        order = order,
        codecName = transportType.codecName(),
        registerOffset = registerOffset,
        bitOffset = bitOffset,
        registerWidth = transportType.registerWidth(length),
        length = length,
        doc = description.cleanDocSummary() ?: name,
    )

private fun CodegenContextDetailDto.modbusTransport(): ModbusTransportKind =
    when (protocolTemplateCode) {
        "MODBUS_TCP_CLIENT" -> ModbusTransportKind.TCP
        else -> ModbusTransportKind.RTU
    }

private fun buildRequestClassName(
    interfaceSimpleName: String,
    transport: ModbusTransportKind,
    methodName: String,
): String {
    val prefix = interfaceSimpleName + transport.transportId.replaceFirstChar(Char::uppercase)
    return prefix + methodName.replaceFirstChar(Char::uppercase) + "Request"
}

private fun buildRequestQualifiedName(
    interfaceSimpleName: String,
    transport: ModbusTransportKind,
    methodName: String,
): String =
    "$MCU_DEVICE_PACKAGE.generated.${buildRequestClassName(interfaceSimpleName, transport, methodName)}"

private fun CodegenTransportType.codecName(): String =
    when (this) {
        CodegenTransportType.BOOL_COIL -> "BOOL_COIL"
        CodegenTransportType.U8 -> "U8"
        CodegenTransportType.U16 -> "U16"
        CodegenTransportType.U32_BE -> "U32_BE"
        CodegenTransportType.BYTE_ARRAY -> "BYTE_ARRAY"
        CodegenTransportType.STRING_ASCII -> "STRING_ASCII"
        CodegenTransportType.STRING_UTF8 -> "STRING_UTF8"
    }

private fun CodegenTransportType.valueKind(): ModbusValueKind =
    when (this) {
        CodegenTransportType.BOOL_COIL -> ModbusValueKind.BOOLEAN
        CodegenTransportType.U8,
        CodegenTransportType.U16,
        CodegenTransportType.U32_BE -> ModbusValueKind.INT
        CodegenTransportType.BYTE_ARRAY -> ModbusValueKind.BYTES
        CodegenTransportType.STRING_ASCII,
        CodegenTransportType.STRING_UTF8 -> ModbusValueKind.STRING
    }

private fun CodegenTransportType.kotlinQualifiedTypeName(): String =
    when (this) {
        CodegenTransportType.BOOL_COIL -> "kotlin.Boolean"
        CodegenTransportType.U8,
        CodegenTransportType.U16,
        CodegenTransportType.U32_BE -> "kotlin.Int"
        CodegenTransportType.BYTE_ARRAY -> "kotlin.ByteArray"
        CodegenTransportType.STRING_ASCII,
        CodegenTransportType.STRING_UTF8 -> "kotlin.String"
    }

private fun CodegenTransportType.registerWidth(
    length: Int,
): Int =
    when (this) {
        CodegenTransportType.BOOL_COIL,
        CodegenTransportType.U8,
        CodegenTransportType.U16 -> 1
        CodegenTransportType.U32_BE -> 2
        CodegenTransportType.BYTE_ARRAY -> (length + 1) / 2
        CodegenTransportType.STRING_ASCII,
        CodegenTransportType.STRING_UTF8 -> length
    }

internal fun String.normalizeGeneratedKotlin(): String =
    lineSequence()
        .joinToString("\n") { line -> line.trimEnd() }
        .trimEnd() + "\n"

internal fun String?.cleanDocSummary(): String? =
    this?.trim()?.takeIf(String::isNotBlank)

private fun String.appendUpsertClause(
    connection: Connection,
): String =
    when {
        connection.isMySql() ->
            this +
                """

                ON DUPLICATE KEY UPDATE
                    context_code = VALUES(context_code),
                    context_name = VALUES(context_name),
                    enabled = VALUES(enabled),
                    consumer_target = VALUES(consumer_target),
                    protocol_template_code = VALUES(protocol_template_code),
                    selected = VALUES(selected),
                    payload = VALUES(payload),
                    updated_at = VALUES(updated_at)
                """.trimIndent()

        else -> error("Unsupported metadata snapshot database: ${connection.metaData.databaseProductName}")
    }

private fun PreparedStatement.setUpdatedAt(
    index: Int,
    connection: Connection,
    instant: Instant,
) {
    check(connection.isMySql()) {
        "Unsupported metadata snapshot database: ${connection.metaData.databaseProductName}"
    }
    setTimestamp(index, java.sql.Timestamp.from(instant))
}

private fun Connection.isMySql(): Boolean =
    metaData.databaseProductName.contains("mysql", ignoreCase = true) ||
        metaData.url.contains(":mysql:", ignoreCase = true)
